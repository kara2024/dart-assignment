void main() {
  // Declaring and initializing variables of type int
  int a = 5;
  int b = 10;

  // Performing arithmetic operations using int variables
  int sum = a + b;
  int difference = a - b;
  int product = a * b;
  int quotient = b ~/ a; // Quotient is Integer division

  // Printing the results
  print('Sum: $sum');
  print('Difference: $difference');
  print('Product: $product');
  print('Quotient: $quotient');
}
