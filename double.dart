void main() {
  // Declaring and initializing variables of type double
  double x = 3.14;
  double y = 2.5;

  // Performing arithmetic operations using double variables
  double sum = x + y;
  double difference = x - y;
  double product = x * y;
  double quotient = x / y;

  // Printing the results
  print('Sum: $sum');
  print('Difference: $difference');
  print('Product: $product');
  print('Quotient: $quotient');
}
